@{
        CIS         = "9.3.4"
        Title       = "Secure transfer required"
        Severity    = "High"
        Automated   = $true
        Recommendation = ""
        Check       = {
            param($D)
    $sa = $D.StorageAccounts
        if (-not $sa) { return @() }
        $bad = @($sa | Where-Object { $_.PSObject.Properties['httpsOnly'] -and -not $_.httpsOnly })
        if ($bad.Count -gt 0) {
            return @(@{ AffectedResources=@($bad | ForEach-Object { $_.storageAccountName } | Select-Object -First 500); Description="$($bad.Count) storage account(s) without HTTPS-only transfer." })
        }
        return @()
        }
    }

