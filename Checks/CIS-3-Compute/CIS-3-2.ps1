@{
        CIS         = "3.2"
        Title       = "Trusted Launch enabled on VMs"
        Severity    = "High"
        Automated   = $true
        Recommendation = ""
        Check       = {
            param($D)
    $vms = $D.VirtualMachines
        if ($null -eq $vms -or @($vms).Count -eq 0) { return @() }
        $bad = @($vms | Where-Object {
            -not $_.PSObject.Properties['securityType'] -or
            ($_.securityType -ne 'TrustedLaunch' -and $_.securityType -ne 'ConfidentialVM')
        })
        if ($bad.Count -gt 0) {
            return @(@{ AffectedResources=@($bad | ForEach-Object { $_.vmName } | Select-Object -First 500); Description="$($bad.Count) VM(s) without Trusted Launch enabled." })
        }
        return @()
        }
    }

