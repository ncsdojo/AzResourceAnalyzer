@{
        CIS         = "8.3.2"
        Title       = "Key expiration set (access policy vaults)"
        Severity    = "Medium"
        Automated   = $true
        Recommendation = ""
        Check       = {
            param($D)
    $keys = $D.KeyVaultKeys
        $kvs = $D.KeyVaults
        if (-not $keys -or -not $kvs) { return @(@{ Manual=$true; Recommendation="Collect KeyVaultKeys via data plane API to check key expiration." }) }
        $apVaults = @($kvs | Where-Object { -not $_.enableRbacAuthorization } | ForEach-Object { $_.vaultName })
        $bad = @($keys | Where-Object { $_.vaultName -in $apVaults -and (-not $_.PSObject.Properties['attributes'] -or -not $_.attributes.exp) })
        if ($bad.Count -gt 0) {
            return @(@{ AffectedResources=@($bad | ForEach-Object { "$($_.vaultName)/$($_.kid -replace '.*/keys/','')" } | Select-Object -First 500); Description="$($bad.Count) key(s) in access-policy vaults without expiration date." })
        }
        return @()
        }
    }

